                $(element).text(Math.floor(this.count));
            },
            complete: function() {
                $(element).text(target);
            }
        });
    }
    
    // Trigger counter when hero section is visible
    var heroAnimated = false;
    $(window).on('scroll', function() {
        var heroSection = $('.survey-hero-section');
        if (heroSection.length && !heroAnimated) {
            var heroTop = heroSection.offset().top;
            var heroBottom = heroTop + heroSection.outerHeight();
            var viewportTop = $(window).scrollTop();
            var viewportBottom = viewportTop + $(window).height();
            
            if (viewportBottom > heroTop && viewportTop < heroBottom) {
                heroAnimated = true;
                $('.stat-number').each(function() {
                    var target = parseInt($(this).data('count'));
                    animateCounter(this, target, 2000);
                });
            }
        }
    });
    
    // Animate categories on scroll
    function animateOnScroll() {
        $('.fade-slide').each(function(index) {
            var element = $(this);
            var elementTop = element.offset().top;
            var elementBottom = elementTop + element.outerHeight();
            var viewportTop = $(window).scrollTop();
            var viewportBottom = viewportTop + $(window).height();
            
            if (viewportBottom > elementTop + 100 && viewportTop < elementBottom) {
                setTimeout(function() {
                    element.css({
                        'opacity': '1',
                        'transform': 'translateY(0)'
                    });
                }, index * 100);
            }
        });
        
        $('.slide-in-left').each(function(index) {
            var element = $(this);
            var elementTop = element.offset().top;
            var elementBottom = elementTop + element.outerHeight();
            var viewportTop = $(window).scrollTop();
            var viewportBottom = viewportTop + $(window).height();
            
            if (viewportBottom > elementTop + 100 && viewportTop < elementBottom) {
                if (!element.hasClass('animated')) {
                    element.addClass('animated');
                }
            }
        });
    }
    
    // Button Click Animation
    $('#surveyMainBtn').on('click', function(e) {
        var button = $(this);
        
        // Create ripple effect
        var ripple = $('<span class="click-ripple"></span>');
        button.append(ripple);
        
        setTimeout(function() {
            ripple.remove();
        }, 600);
        
        // Add shake animation
        button.addClass('btn-shake');
        setTimeout(function() {
            button.removeClass('btn-shake');
        }, 500);
    });
    
    // Smooth scroll behavior
    $('a[href^="#"]').on('click', function(e) {
        e.preventDefault();
        var target = $(this.getAttribute('href'));
        if (target.length) {
            $('html, body').stop().animate({
                scrollTop: target.offset().top - 80
            }, 1000, 'easeInOutCubic');
        }
    });
    
    // Parallax effect for hero shapes
    $(window).on('scroll', function() {
        var scrolled = $(window).scrollTop();
        $('.survey-hero-section::before').css('transform', 'translateY(' + (scrolled * 0.3) + 'px)');
    });
    
    // Hover effect for category cards
    $('.category-group').hover(
        function() {
            $(this).find('.target-item').each(function(index) {
                var item = $(this);
                setTimeout(function() {
                    item.css({
                        'transform': 'translateX(5px)',
                        'background': '#e6f7ff'
                    });
                }, index * 50);
            });
        },
        function() {
            $(this).find('.target-item').css({
                'transform': 'translateX(0)',
                'background': ''
            });
        }
    );
    
    // Initialize animations on page load
    animateOnScroll();
    
    // Trigger animations on scroll
    $(window).on('scroll', animateOnScroll);
    
    // Trigger initial animation
    $(window).trigger('scroll');
});

// Add easing function
jQuery.easing.easeInOutCubic = function(x, t, b, c, d) {
    if ((t/=d/2) < 1) return c/2*t*t*t + b;
    return c/2*((t-=2)*t*t + 2) + b;
};
